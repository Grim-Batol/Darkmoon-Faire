-- creature_template  —  TrinityCore `tc_world` -> AzerothCore `prod_world` (map 974)
INSERT IGNORE INTO `prod_world`.`creature_template`
  (`entry`,`difficulty_entry_1`,`difficulty_entry_2`,`difficulty_entry_3`,`KillCredit1`,`KillCredit2`,`name`,`subname`,`IconName`,`gossip_menu_id`,`minlevel`,`maxlevel`,`exp`,`faction`,`npcflag`,`speed_walk`,`speed_run`,`speed_swim`,`speed_flight`,`detection_range`,`rank`,`dmgschool`,`DamageModifier`,`BaseAttackTime`,`RangeAttackTime`,`BaseVariance`,`RangeVariance`,`unit_class`,`unit_flags`,`unit_flags2`,`dynamicflags`,`family`,`type`,`type_flags`,`lootid`,`pickpocketloot`,`skinloot`,`PetSpellDataId`,`VehicleId`,`mingold`,`maxgold`,`AIName`,`MovementType`,`HoverHeight`,`HealthModifier`,`ManaModifier`,`ArmorModifier`,`ExperienceModifier`,`RacialLeader`,`movementId`,`RegenHealth`,`CreatureImmunitiesId`,`flags_extra`,`ScriptName`,`VerifiedBuild`)
SELECT
  t.`entry` AS `entry`,
  0 AS `difficulty_entry_1`,
  0 AS `difficulty_entry_2`,
  0 AS `difficulty_entry_3`,
  t.`KillCredit1` AS `KillCredit1`,
  t.`KillCredit2` AS `KillCredit2`,
  LEFT(COALESCE(t.`name`,''),100) AS `name`,
  LEFT(COALESCE(t.`subname`,''),100) AS `subname`,
  LEFT(COALESCE(t.`IconName`,''),100) AS `IconName`,
  0 AS `gossip_menu_id`,
  80 AS `minlevel`,
  80 AS `maxlevel`,
  2 AS `exp`,
  t.`faction` AS `faction`,
  (t.`npcflag` & 0xFFFFFFFF) AS `npcflag`,
  t.`speed_walk` AS `speed_walk`,
  t.`speed_run` AS `speed_run`,
  1 AS `speed_swim`,
  1 AS `speed_flight`,
  18 AS `detection_range`,
  t.`Classification` AS `rank`,
  t.`dmgschool` AS `dmgschool`,
  COALESCE(d.`DamageModifier`,1) AS `DamageModifier`,
  t.`BaseAttackTime` AS `BaseAttackTime`,
  t.`RangeAttackTime` AS `RangeAttackTime`,
  t.`BaseVariance` AS `BaseVariance`,
  t.`RangeVariance` AS `RangeVariance`,
  IF(t.`unit_class` IN (1,2,4,8),t.`unit_class`,1) AS `unit_class`,
  t.`unit_flags` AS `unit_flags`,
  t.`unit_flags2` AS `unit_flags2`,
  0 AS `dynamicflags`,
  IF(t.`family` BETWEEN 0 AND 127, t.`family`, 0) AS `family`,
  t.`type` AS `type`,
  COALESCE(d.`TypeFlags`,0) AS `type_flags`,
  COALESCE(d.`LootID`,0) AS `lootid`,
  COALESCE(d.`PickPocketLootID`,0) AS `pickpocketloot`,
  COALESCE(d.`SkinLootID`,0) AS `skinloot`,
  0 AS `PetSpellDataId`,
  t.`VehicleId` AS `VehicleId`,
  COALESCE(d.`GoldMin`,0) AS `mingold`,
  COALESCE(d.`GoldMax`,0) AS `maxgold`,
  t.`AIName` AS `AIName`,
  t.`MovementType` AS `MovementType`,
  1 AS `HoverHeight`,
  COALESCE(d.`HealthModifier`,1) AS `HealthModifier`,
  COALESCE(d.`ManaModifier`,1) AS `ManaModifier`,
  COALESCE(d.`ArmorModifier`,1) AS `ArmorModifier`,
  t.`ExperienceModifier` AS `ExperienceModifier`,
  t.`RacialLeader` AS `RacialLeader`,
  t.`movementId` AS `movementId`,
  t.`RegenHealth` AS `RegenHealth`,
  0 AS `CreatureImmunitiesId`,
  (t.`flags_extra` & 0x0FFFFFFF) AS `flags_extra`,
  '' AS `ScriptName`,
  0 AS `VerifiedBuild`
FROM `tc_world`.`creature_template` t
LEFT JOIN `tc_world`.`creature_template_difficulty` d ON d.`Entry` = t.`entry` AND d.`DifficultyID` = 0
WHERE t.`entry` IN (SELECT DISTINCT `id` FROM `tc_world`.`creature` WHERE `map` = 974);
